#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned long int	i;
	unsigned char		*t;

	i = 0;
	t = (unsigned char *)s;
	while (i < n)
	{
		t[i] = (unsigned char)c;
		i++;
	}
	return ((void *)(s));
}
