#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	nb;

	if (n <= -2147483647)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	nb = n % 10;
	n = n / 10;
	if (n != 0)
		ft_putnbr_fd(n, fd);
	nb += 48;
	write(fd, &nb, 1);
}
